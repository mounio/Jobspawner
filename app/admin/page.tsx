'use client';
import { useEffect, useState } from 'react';
import { useRouter } from 'next/navigation';
import { useAuth } from '@/context/AuthContext';
import { useToast } from '@/context/ToastContext';
import { jobsApi, adminApi } from '@/lib/api';
import type { ScrapingResultDto, ScrapeSourceResultDto } from '@/lib/types';
import { formatDate } from '@/lib/utils';

type SourceKey = 'github' | 'stackoverflow' | 'remoteok' | 'weworkremotely' | 'linkedin';

const SOURCES: { key: SourceKey; label: string; icon: string; desc: string }[] = [
  { key: 'github',        label: 'GitHub',        icon: '🐙', desc: 'Issues "hiring"'       },
  { key: 'stackoverflow', label: 'Stack Overflow', icon: '📚', desc: 'RSS Feed'              },
  { key: 'remoteok',      label: 'Remote OK',      icon: '🌍', desc: 'API JSON'              },
  { key: 'weworkremotely',label: 'WeWorkRemotely', icon: '🏠', desc: 'RSS catégories'        },
  { key: 'linkedin',      label: 'LinkedIn',       icon: '💼', desc: 'Guest jobs API'        },
];

export default function AdminPage() {
  const { user, isLoading, isAdmin } = useAuth();
  const  toast  = useToast();
  const router    = useRouter();

  const [total,       setTotal]       = useState<number | null>(null);
  const [refreshRes,  setRefreshRes]  = useState<ScrapingResultDto | null>(null);
  const [scrapeRes,   setScrapeRes]   = useState<ScrapeSourceResultDto | null>(null);
  const [activeSource,setActiveSource]= useState<SourceKey | null>(null);
  const [running,     setRunning]     = useState(false);
  const [limit,       setLimit]       = useState(50);

  useEffect(() => {
    if (isLoading) return;
    if (!user || !isAdmin) { router.replace('/dashboard'); return; }
    jobsApi.advanced({ pageNumber: 1, pageSize: 1, sortBy: 'newest' })
      .then(d => setTotal(d.totalCount))
      .catch(() => {});
  }, [isLoading, user]);

  const reloadTotal = () =>
    jobsApi.advanced({ pageNumber: 1, pageSize: 1, sortBy: 'newest' })
      .then(d => setTotal(d.totalCount)).catch(() => {});

  const handleRefreshAll = async () => {
    setRunning(true); setRefreshRes(null); setScrapeRes(null); setActiveSource(null);
    try {
      const res = await adminApi.refreshAll();
      setRefreshRes(res);
      toast(`✅ ${res.totalImported} offres importées`, 'success');
      reloadTotal();
    } catch (err: any) { toast(err.message ?? 'Erreur scraping', 'error'); }
    finally { setRunning(false); }
  };

  const handleScrapeSource = async (source: SourceKey) => {
    setRunning(true); setRefreshRes(null); setScrapeRes(null); setActiveSource(source);
    try {
      const res = await adminApi.scrapeSource(source, limit);
      setScrapeRes(res);
      toast(`✅ ${res.imported} offres depuis ${res.source}`, 'success');
      reloadTotal();
    } catch (err: any) { toast(err.message ?? 'Erreur scraping', 'error'); }
    finally { setRunning(false); }
  };

  if (isLoading || !user) return null;

  return (
    <main style={{ maxWidth: 900, margin: '0 auto', padding: '2.5rem 1.5rem' }}>
      <h1 style={{ fontSize: '1.75rem', fontWeight: 900, margin: '0 0 4px', color: 'var(--color-text)' }}>⚙️ Admin — Scraping</h1>
      <p style={{ color: 'var(--color-text-muted)', marginBottom: '2rem', fontSize: '0.9rem' }}>
        Le scraping automatique se lance toutes les 6h. Vous pouvez aussi l&apos;activer manuellement ci-dessous.
      </p>

      {/* Total stat */}
      {total !== null && (
        <div style={{ display: 'flex', gap: 16, alignItems: 'center', marginBottom: '2rem', background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, padding: '1.25rem 1.5rem' }}>
          <span style={{ fontSize: '2rem' }}>📊</span>
          <div>
            <p style={{ fontSize: '1.6rem', fontWeight: 900, color: 'var(--color-cyan)', margin: 0 }}>{total}</p>
            <p style={{ color: 'var(--color-text-muted)', fontSize: '0.82rem', margin: 0 }}>offres en base de données</p>
          </div>
        </div>
      )}

      {/* Limit + Refresh ALL */}
      <div style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, padding: '1.5rem', marginBottom: '1.5rem' }}>
        <div style={{ display: 'flex', gap: 16, alignItems: 'flex-end', marginBottom: '1.25rem', flexWrap: 'wrap' }}>
          <div>
            <label style={{ display: 'block', fontSize: '0.72rem', fontWeight: 700, color: 'var(--color-text-muted)', marginBottom: 6 }}>LIMITE PAR SOURCE</label>
            <input type="number" className="input" style={{ width: 100 }} min={1} max={200} value={limit} onChange={e => setLimit(Number(e.target.value))} disabled={running} />
          </div>
          <div style={{ fontSize: '0.8rem', color: 'var(--color-text-muted)', lineHeight: 1.5, maxWidth: 360 }}>
            Le refresh automatique se déclenche toutes les <strong>6h</strong> au démarrage du backend.
          </div>
        </div>
        <button className="btn-primary" style={{ width: '100%', padding: '0.9rem', fontSize: '1rem' }}
          onClick={handleRefreshAll} disabled={running}>
          {running && !activeSource ? '⏳ Scraping en cours (15-30s)…' : '🚀 Rafraîchir toutes les sources'}
        </button>
      </div>

      {/* Individual sources */}
      <div style={{ display: 'grid', gap: 12, gridTemplateColumns: 'repeat(auto-fill, minmax(170px, 1fr))', marginBottom: '1.5rem' }}>
        {SOURCES.map(s => (
          <div key={s.key} style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 12, padding: '1.25rem' }}>
            <div style={{ fontSize: '1.75rem', marginBottom: 8 }}>{s.icon}</div>
            <p style={{ fontWeight: 700, margin: '0 0 4px', fontSize: '0.9rem', color: 'var(--color-text)' }}>{s.label}</p>
            <p style={{ fontSize: '0.75rem', color: 'var(--color-text-muted)', marginBottom: '0.9rem' }}>{s.desc}</p>
            {refreshRes?.importedBySource && s.label in (refreshRes.importedBySource ?? {}) && (
              <p style={{ fontSize: '0.72rem', fontWeight: 700, color: '#10b981', marginBottom: 8 }}>
                ✓ {refreshRes.importedBySource[s.label]} importés
              </p>
            )}
            <button className="btn-secondary" style={{ width: '100%', fontSize: '0.82rem' }}
              onClick={() => handleScrapeSource(s.key)} disabled={running}>
              {running && activeSource === s.key ? '⏳…' : 'Lancer'}
            </button>
          </div>
        ))}
      </div>

      {/* Results */}
      {refreshRes && (
        <div style={{ background: 'var(--color-card)', border: '1px solid rgba(16,185,129,0.3)', borderRadius: 14, padding: '1.5rem' }}>
          <h3 style={{ fontWeight: 700, marginBottom: '1rem', color: '#10b981' }}>✅ Résultat — Refresh complet</h3>
          <p style={{ marginBottom: 8 }}><strong>Total importé :</strong> {refreshRes.totalImported} offres</p>
          {refreshRes.importedBySource && (
            <div style={{ marginBottom: 8 }}>
              <strong>Par source :</strong>
              <ul style={{ marginTop: 6, paddingLeft: 20 }}>
                {Object.entries(refreshRes.importedBySource).map(([src, c]) => (
                  <li key={src} style={{ color: 'var(--color-text-muted)', fontSize: '0.88rem' }}>{src} → <strong>{c}</strong></li>
                ))}
              </ul>
            </div>
          )}
          {refreshRes.errors?.length ? (
            <div style={{ color: '#ef4444', fontSize: '0.82rem' }}>
              <strong>Erreurs :</strong>
              <ul style={{ marginTop: 4, paddingLeft: 20 }}>{refreshRes.errors.map((e, i) => <li key={i}>{e}</li>)}</ul>
            </div>
          ) : null}
          <p style={{ color: 'var(--color-text-muted)', fontSize: '0.78rem', marginTop: 12 }}>Complété le {formatDate(refreshRes.completedAt)}</p>
        </div>
      )}

      {scrapeRes && (
        <div style={{ background: 'var(--color-card)', border: '1px solid rgba(16,185,129,0.3)', borderRadius: 14, padding: '1.5rem' }}>
          <h3 style={{ fontWeight: 700, color: '#10b981', marginBottom: 8 }}>✅ {scrapeRes.source}</h3>
          <p><strong>{scrapeRes.imported}</strong> offres importées depuis <strong>{scrapeRes.source}</strong></p>
        </div>
      )}
    </main>
  );
}





















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































