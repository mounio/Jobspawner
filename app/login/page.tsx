'use client';
import { useState, useEffect } from 'react';
import Link from 'next/link';
import { useRouter } from 'next/navigation';
import { authApi } from '@/lib/api';
import { AuthResponseDto } from '@/lib/types';
import { useAuth } from '@/context/AuthContext';
import { useToast } from '@/context/ToastContext';

export default function LoginPage() {
  const { user, login } = useAuth();
  const router = useRouter();
  const toast  = useToast();
  const [form, setForm]       = useState({ email: '', password: '' });
  const [loading, setLoading] = useState(false);
  const [error, setError]     = useState('');

  useEffect(() => { if (user) router.replace('/dashboard'); }, [user]);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError('');
    setLoading(true);
    try {
      const res = await authApi.login({
        email:    form.email.trim().toLowerCase(),
        password: form.password,
      }) as AuthResponseDto;

      if (!res?.token) {
        setError(res?.message ?? 'Identifiants incorrects.');
        return;
      }

      login(res.token, res.user);
      toast('Connexion réussie ! Bienvenue ' + res.user.firstName);
      router.push('/dashboard');

    } catch (err: any) {
      const msg = err?.message ?? 'Identifiants incorrects.';
      if (err?.status === 401 || msg.toLowerCase().includes('invalide') || msg.toLowerCase().includes('incorrect')) {
        setError('Email ou mot de passe incorrect.');
      } else if (err?.status === 500) {
        setError('Erreur serveur (500). Vérifiez que le backend et la base de données sont démarrés.');
      } else {
        setError(msg);
      }
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="grid-bg" style={{ minHeight: 'calc(100vh - 64px)', display: 'flex', alignItems: 'center', justifyContent: 'center', padding: '2rem 1rem' }}>
      <div style={{ width: '100%', maxWidth: 420 }}>
        <div style={{ textAlign: 'center', marginBottom: '2.5rem' }}>
          <div style={{ display: 'inline-flex', width: 52, height: 52, borderRadius: 14, background: 'linear-gradient(135deg,#00c8ff,#8b5cf6)', alignItems: 'center', justifyContent: 'center', fontSize: 24, fontWeight: 800, color: '#000', marginBottom: '1.25rem' }}>J</div>
          <h1 style={{ fontSize: '1.75rem', fontWeight: 900, margin: '0 0 8px', color: 'var(--color-text)' }}>Bon retour !</h1>
          <p style={{ color: 'var(--color-muted)', fontSize: '0.88rem', margin: 0 }}>Connectez-vous à votre compte JobSpawner</p>
        </div>

        <div style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 18, padding: '2rem' }}>
          {error && (
            <div style={{ background: 'rgba(239,68,68,0.08)', border: '1px solid rgba(239,68,68,0.35)', borderRadius: 10, padding: '0.875rem 1rem', marginBottom: '1.25rem', color: '#fca5a5', fontSize: '0.83rem', lineHeight: 1.6 }}>
              ⚠ {error}
            </div>
          )}

          <form onSubmit={handleSubmit} style={{ display: 'flex', flexDirection: 'column', gap: 16 }}>
            <div>
              <label style={{ display: 'block', fontSize: '0.75rem', fontWeight: 700, color: 'var(--color-muted)', marginBottom: 6, letterSpacing: '0.05em' }}>EMAIL</label>
              <input className="input-dark" type="email" required autoFocus
                value={form.email} onChange={e => setForm(f => ({ ...f, email: e.target.value }))}
                placeholder="vous@exemple.com" />
            </div>
            <div>
              <label style={{ display: 'block', fontSize: '0.75rem', fontWeight: 700, color: 'var(--color-muted)', marginBottom: 6, letterSpacing: '0.05em' }}>MOT DE PASSE</label>
              <input className="input-dark" type="password" required
                value={form.password} onChange={e => setForm(f => ({ ...f, password: e.target.value }))}
                placeholder="••••••••" autoComplete="current-password" />
            </div>

            <button type="submit" className="btn-primary" disabled={loading}
              style={{ width: '100%', justifyContent: 'center', height: 46, fontSize: '0.95rem', marginTop: 4 }}>
              {loading
                ? <><span className="animate-spin" style={{ display: 'inline-block' }}>⟳</span>&nbsp;Connexion…</>
                : 'Se connecter →'
              }
            </button>
          </form>
        </div>

        <p style={{ textAlign: 'center', marginTop: '1.5rem', color: 'var(--color-muted)', fontSize: '0.85rem' }}>
          Pas encore de compte ?{' '}
          <Link href="/register" style={{ color: 'var(--color-cyan)', fontWeight: 600, textDecoration: 'none' }}>
            S&apos;inscrire
          </Link>
        </p>
      </div>
    </div>
  );
}





















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































