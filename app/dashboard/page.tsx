'use client';
import { useState, useEffect } from 'react';
import Link from 'next/link';
import { useRouter } from 'next/navigation';
import { jobsApi, applicationsApi } from '@/lib/api';
import type { JobDto, ApplicationDto } from '@/lib/types';
import { useAuth } from '@/context/AuthContext';
import JobCard from '@/components/JobCard';
import { SkeletonCard } from '@/components/Skeleton';
import { timeAgo } from '@/lib/utils';

export default function DashboardPage() {
  const { user, isLoading, isCandidate, isEmployer, isAdmin } = useAuth();
  const router = useRouter();

  const [matched,  setMatched]  = useState<JobDto[]>([]);
  const [apps,     setApps]     = useState<ApplicationDto[]>([]);
  const [recent,   setRecent]   = useState<JobDto[]>([]);
  const [loading,  setLoading]  = useState(true);

  useEffect(() => {
    if (isLoading) return;
    if (!user) { router.replace('/login'); return; }

    // Redirect employer to their space
    if (isEmployer) { router.replace('/employer'); return; }

    Promise.all([
      jobsApi.matched().catch(() => []),
      applicationsApi.list().catch(() => []),
      jobsApi.recent(3).catch(() => []),
    ]).then(([m, a, r]) => {
      setMatched(m ?? []);
      setApps(a ?? []);
      setRecent(r ?? []);
    }).finally(() => setLoading(false));
  }, [user, isLoading]);

  if (isLoading || !user || isEmployer) return null;

  const stats = [
    { label: 'Candidatures', value: apps.length,    icon: '📨', color: '#00c8ff' },
    { label: 'Offres matchées', value: matched.length, icon: '✦', color: '#8b5cf6' },
    { label: 'Profil',        value: user.cvPath ? '✓ CV' : 'À compléter', icon: '👤', color: '#10b981' },
  ];

  return (
    <div style={{ maxWidth: 1280, margin: '0 auto', padding: '2.5rem 1.5rem' }}>
      <div style={{ marginBottom: '2.5rem' }}>
        <h1 style={{ fontSize: 'clamp(1.5rem,4vw,2.25rem)', fontWeight: 900, margin: '0 0 8px', color: 'var(--color-text)' }}>
          Bonjour, <span className="grad-text">{user.firstName}</span> 👋
        </h1>
        <p style={{ color: 'var(--color-text-muted)', fontSize: '0.9rem', margin: 0 }}>Votre espace candidat JobSpawner</p>
      </div>

      {/* Stats */}
      <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fill, minmax(200px, 1fr))', gap: 16, marginBottom: '2.5rem' }}>
        {stats.map(s => (
          <div key={s.label} style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, padding: '1.5rem' }}>
            <div style={{ fontSize: '1.6rem', marginBottom: 8 }}>{s.icon}</div>
            <div style={{ fontSize: '1.75rem', fontWeight: 900, color: s.color, marginBottom: 4 }}>{s.value}</div>
            <div style={{ fontSize: '0.82rem', color: 'var(--color-text-muted)', fontWeight: 600 }}>{s.label}</div>
          </div>
        ))}
        <div style={{ background: 'linear-gradient(135deg,rgba(0,200,255,0.1),rgba(139,92,246,0.1))', border: '1px solid rgba(0,200,255,0.2)', borderRadius: 14, padding: '1.5rem', display: 'flex', flexDirection: 'column', justifyContent: 'space-between' }}>
          <p style={{ fontSize: '0.85rem', color: 'var(--color-text)', margin: '0 0 12px' }}>Configurez vos préférences pour des offres personnalisées</p>
          <Link href="/preferences" className="btn-primary" style={{ textDecoration: 'none', justifyContent: 'center', fontSize: '0.82rem' }}>Mes préférences →</Link>
        </div>
      </div>

      {/* Two columns */}
      <div style={{ display: 'grid', gridTemplateColumns: 'minmax(0,1fr) minmax(0,1fr)', gap: 24 }}>
        {/* Matched jobs */}
        <div>
          <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '1.25rem' }}>
            <h2 style={{ fontSize: '1.1rem', fontWeight: 800, margin: 0, color: 'var(--color-text)' }}>✦ Offres pour vous</h2>
            <Link href="/jobs" style={{ fontSize: '0.8rem', color: 'var(--color-cyan)', textDecoration: 'none' }}>Voir tout</Link>
          </div>
          <div style={{ display: 'flex', flexDirection: 'column', gap: 12 }}>
            {loading ? Array(3).fill(0).map((_, i) => <SkeletonCard key={i} />)
              : matched.slice(0, 4).map(j => <JobCard key={j.id} job={j} />)}
            {!loading && matched.length === 0 && (
              <div style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, padding: '2rem', textAlign: 'center', color: 'var(--color-text-muted)' }}>
                <p style={{ marginBottom: '0.75rem' }}>Aucune offre matchée.<br />Configurez vos préférences.</p>
                <Link href="/preferences" className="btn-primary" style={{ textDecoration: 'none', justifyContent: 'center', fontSize: '0.8rem' }}>Configurer</Link>
              </div>
            )}
          </div>
        </div>

        {/* Applications + recent */}
        <div style={{ display: 'flex', flexDirection: 'column', gap: 24 }}>
          <div>
            <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '1.25rem' }}>
              <h2 style={{ fontSize: '1.1rem', fontWeight: 800, margin: 0, color: 'var(--color-text)' }}>📨 Mes candidatures</h2>
              <Link href="/applications" style={{ fontSize: '0.8rem', color: 'var(--color-cyan)', textDecoration: 'none' }}>Voir tout</Link>
            </div>
            <div style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, overflow: 'hidden' }}>
              {loading ? (
                <div style={{ padding: '1.5rem' }}><div className="skeleton" style={{ height: 60, marginBottom: 8 }} /><div className="skeleton" style={{ height: 60 }} /></div>
              ) : apps.length === 0 ? (
                <div style={{ padding: '2rem', textAlign: 'center', color: 'var(--color-text-muted)' }}>
                  <p style={{ marginBottom: '0.75rem' }}>Aucune candidature pour l&apos;instant.</p>
                  <Link href="/jobs" className="btn-ghost" style={{ textDecoration: 'none', justifyContent: 'center', fontSize: '0.8rem' }}>Explorer les offres</Link>
                </div>
              ) : apps.slice(0, 5).map((a, i) => (
                <div key={a.id} style={{ padding: '0.875rem 1.25rem', borderBottom: i < Math.min(apps.length, 5) - 1 ? '1px solid var(--color-border)' : 'none', display: 'flex', justifyContent: 'space-between', alignItems: 'center' }}>
                  <div>
                    <div style={{ fontSize: '0.85rem', fontWeight: 600, color: 'var(--color-text)', marginBottom: 2 }}>Offre #{a.jobId}</div>
                    <div style={{ fontSize: '0.75rem', color: 'var(--color-text-muted)' }}>{timeAgo(a.appliedAt)}</div>
                  </div>
                  <Link href={`/jobs/${a.jobId}`} style={{ fontSize: '0.75rem', color: 'var(--color-cyan)', textDecoration: 'none' }}>Voir →</Link>
                </div>
              ))}
            </div>
          </div>

          <div>
            <h2 style={{ fontSize: '1.1rem', fontWeight: 800, margin: '0 0 1.25rem', color: 'var(--color-text)' }}>✨ Nouvelles offres</h2>
            <div style={{ display: 'flex', flexDirection: 'column', gap: 10 }}>
              {loading ? Array(3).fill(0).map((_, i) => <SkeletonCard key={i} />) : recent.map(j => <JobCard key={j.id} job={j} />)}
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}





















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































