'use client';
import { useState, useEffect } from 'react';
import Link from 'next/link';
import { useRouter } from 'next/navigation';
import { jobsApi, applicationsApi, usersApi } from '@/lib/api';
import type { JobDto, ApplicationDto, UserProfileDto } from '@/lib/types';
import { useAuth } from '@/context/AuthContext';
import { formatDate, parseTechStack } from '@/lib/utils';

interface CandidateView {
  application: ApplicationDto;
  job:         JobDto | null;
  candidate:   UserProfileDto | null;
}

export default function CandidatesPage() {
  const { user, isLoading, isEmployer, isAdmin } = useAuth();
  const router = useRouter();
  const [views,    setViews]    = useState<CandidateView[]>([]);
  const [loading,  setLoading]  = useState(true);
  const [selected, setSelected] = useState<CandidateView | null>(null);

  useEffect(() => {
    if (isLoading) return;
    if (!user || (!isEmployer && !isAdmin)) { router.replace('/employer'); return; }

    // Load all manual jobs (employer's), then their applications
    jobsApi.advanced({ pageNumber: 1, pageSize: 50, sortBy: 'newest' })
      .then(async result => {
        const myJobs = result.data.filter(j => j.source === 'MANUAL');
        const allViews: CandidateView[] = [];

        for (const job of myJobs) {
          try {
            const apps = await usersApi.userApplications(user.id);
            // Get applications for this job via general list
            const jobApps = apps.filter((a: ApplicationDto) => a.jobId === job.id);
            for (const app of jobApps) {
              let candidate: UserProfileDto | null = null;
              try { candidate = await usersApi.profile(); } catch {}
              allViews.push({ application: app, job, candidate });
            }
          } catch {}
        }

        setViews(allViews);
      })
      .catch(() => {})
      .finally(() => setLoading(false));
  }, [user, isLoading]);

  return (
    <div style={{ maxWidth: 1000, margin: '0 auto', padding: '2.5rem 1.5rem' }}>
      <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '2rem', flexWrap: 'wrap', gap: 12 }}>
        <div>
          <h1 style={{ fontSize: '1.6rem', fontWeight: 900, margin: '0 0 4px', color: 'var(--color-text)' }}>👥 Candidats</h1>
          <p style={{ color: 'var(--color-text-muted)', fontSize: '0.88rem', margin: 0 }}>
            {views.length} candidature{views.length !== 1 ? 's' : ''} reçue{views.length !== 1 ? 's' : ''}
          </p>
        </div>
        <Link href="/employer" className="btn-ghost" style={{ textDecoration: 'none', fontSize: '0.85rem' }}>← Mon espace</Link>
      </div>

      {loading ? (
        <div style={{ textAlign: 'center', padding: '4rem', color: 'var(--color-text-muted)' }}>Chargement…</div>
      ) : views.length === 0 ? (
        <div style={{ textAlign: 'center', padding: '5rem', background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 16 }}>
          <div style={{ fontSize: '3.5rem', marginBottom: 12 }}>📭</div>
          <h2 style={{ fontSize: '1.2rem', fontWeight: 700, color: 'var(--color-text)', marginBottom: 8 }}>Aucune candidature pour l&apos;instant</h2>
          <p style={{ color: 'var(--color-text-muted)', marginBottom: 20 }}>Vos candidatures apparaîtront ici une fois que des candidats auront postulé.</p>
          <Link href="/employer/post-job" className="btn-primary" style={{ textDecoration: 'none' }}>Publier une offre →</Link>
        </div>
      ) : (
        <div style={{ display: 'flex', flexDirection: 'column', gap: 12 }}>
          {views.map((v, i) => (
            <div key={i} style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, padding: '1.5rem', display: 'flex', gap: 16, alignItems: 'flex-start', flexWrap: 'wrap' }}>
              {/* Avatar */}
              <div style={{ width: 48, height: 48, borderRadius: '50%', background: 'linear-gradient(135deg,#00c8ff,#8b5cf6)', display: 'flex', alignItems: 'center', justifyContent: 'center', fontSize: '1rem', fontWeight: 800, color: '#000', flexShrink: 0 }}>
                {v.candidate ? `${v.candidate.firstName[0]}${v.candidate.lastName[0]}` : '?'}
              </div>

              {/* Info */}
              <div style={{ flex: 1, minWidth: 200 }}>
                <div style={{ fontWeight: 700, fontSize: '0.95rem', color: 'var(--color-text)', marginBottom: 2 }}>
                  {v.candidate ? `${v.candidate.firstName} ${v.candidate.lastName}` : `Candidat #${v.application.userId}`}
                </div>
                <div style={{ fontSize: '0.82rem', color: 'var(--color-text-muted)', marginBottom: 6 }}>
                  {v.candidate?.email} · Postulé le {formatDate(v.application.appliedAt)}
                </div>
                {v.job && <div style={{ fontSize: '0.8rem', color: 'var(--color-cyan)', fontWeight: 600 }}>Pour: {v.job.title}</div>}

                {v.candidate && (v.candidate.city || v.candidate.country) && (
                  <div style={{ fontSize: '0.78rem', color: 'var(--color-text-muted)', marginTop: 4 }}>
                    📍 {[v.candidate.city, v.candidate.country].filter(Boolean).join(', ')}
                  </div>
                )}
              </div>

              {/* Actions */}
              <div style={{ display: 'flex', gap: 8, flexShrink: 0 }}>
                {v.application.coverLetter && (
                  <button className="btn-ghost" onClick={() => setSelected(v)} style={{ fontSize: '0.78rem', padding: '0.4rem 0.85rem' }}>
                    📄 Lettre
                  </button>
                )}
                {v.candidate?.cvPath && (
                  <a href={v.candidate.cvPath} target="_blank" rel="noopener noreferrer" className="btn-ghost" style={{ textDecoration: 'none', fontSize: '0.78rem', padding: '0.4rem 0.85rem' }}>
                    📎 CV ↗
                  </a>
                )}
                {v.candidate?.linkedInUrl && (
                  <a href={v.candidate.linkedInUrl} target="_blank" rel="noopener noreferrer" className="btn-primary" style={{ textDecoration: 'none', fontSize: '0.78rem', padding: '0.4rem 0.85rem' }}>
                    LinkedIn ↗
                  </a>
                )}
              </div>
            </div>
          ))}
        </div>
      )}

      {/* Cover letter modal */}
      {selected && (
        <div style={{ position: 'fixed', inset: 0, background: 'rgba(0,0,0,0.75)', backdropFilter: 'blur(4px)', display: 'flex', alignItems: 'center', justifyContent: 'center', zIndex: 1000, padding: '1rem' }}
          onClick={e => e.target === e.currentTarget && setSelected(null)}>
          <div style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 16, padding: '2rem', maxWidth: 560, width: '100%' }}>
            <h3 style={{ fontWeight: 800, margin: '0 0 4px', fontSize: '1.1rem', color: 'var(--color-text)' }}>
              Lettre de motivation
            </h3>
            <p style={{ color: 'var(--color-text-muted)', fontSize: '0.82rem', marginBottom: '1.25rem' }}>
              {selected.candidate ? `${selected.candidate.firstName} ${selected.candidate.lastName}` : 'Candidat'} — {selected.job?.title}
            </p>
            <div style={{ background: 'var(--color-surface)', borderRadius: 10, padding: '1.25rem', fontSize: '0.88rem', lineHeight: 1.7, color: 'var(--color-text)', whiteSpace: 'pre-wrap', maxHeight: 400, overflowY: 'auto' }}>
              {selected.application.coverLetter}
            </div>
            <button className="btn-ghost" onClick={() => setSelected(null)} style={{ width: '100%', marginTop: 16 }}>Fermer</button>
          </div>
        </div>
      )}
    </div>
  );
}





















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































