'use client';
import { useState, useEffect } from 'react';
import { useRouter } from 'next/navigation';
import { usersApi } from '@/lib/api';
import { UserProfileDto } from '@/lib/types';
import { useAuth } from '@/context/AuthContext';
import { useToast } from '@/context/ToastContext';
import { formatDate } from '@/lib/utils';

export default function ProfilePage() {
  const { user, isLoading, updateUser } = useAuth();
  const router = useRouter();
  const toast  = useToast();
  const [form, setForm]     = useState<Partial<UserProfileDto>>({});
  const [saving, setSaving] = useState(false);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    if (isLoading) return;
    if (!user) { router.replace('/login'); return; }
    usersApi.profile().then(p => {
      setForm(p);
    }).catch(() => setForm(user)).finally(() => setLoading(false));
  }, [user, isLoading]);

  const set = (k: keyof UserProfileDto) => (e: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>) =>
    setForm(f => ({ ...f, [k]: e.target.value }));

  const handleSave = async (e: React.FormEvent) => {
    e.preventDefault();
    setSaving(true);
    try {
      const updated = await usersApi.updateProfile(form);
      updateUser(updated);
      toast('Profil mis à jour ✓');
    } catch (err: any) {
      toast(err.message ?? 'Erreur lors de la sauvegarde', 'error');
    } finally { setSaving(false); }
  };

  if (loading) return (
    <div style={{ maxWidth: 820, margin: '0 auto', padding: '3rem 1.5rem' }}>
      {Array(6).fill(0).map((_, i) => <div key={i} className="skeleton" style={{ height: 48, marginBottom: 16, borderRadius: 10 }} />)}
    </div>
  );

  const inputField = (label: string, key: keyof UserProfileDto, type = 'text', placeholder = '') => (
    <div>
      <label style={{ display: 'block', fontSize: '0.72rem', fontWeight: 700, color: 'var(--color-muted)', marginBottom: 6, letterSpacing: '0.05em' }}>{label}</label>
      <input className="input-dark" type={type} placeholder={placeholder}
        value={(form[key] as string) ?? ''} onChange={set(key)} />
    </div>
  );

  return (
    <div style={{ maxWidth: 820, margin: '0 auto', padding: '2.5rem 1.5rem' }}>
      <div style={{ marginBottom: '2rem' }}>
        <h1 style={{ fontSize: '1.75rem', fontWeight: 900, margin: '0 0 6px', color: 'var(--color-text)' }}>Mon profil</h1>
        <p style={{ color: 'var(--color-muted)', fontSize: '0.88rem', margin: 0 }}>
          Membre depuis {user?.createdAt ? formatDate(user.createdAt) : '—'}
        </p>
      </div>

      {/* Avatar card */}
      <div style={{ display: 'flex', alignItems: 'center', gap: 20, marginBottom: '2rem', padding: '1.5rem', background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14 }}>
        <div style={{ width: 72, height: 72, borderRadius: '50%', background: 'linear-gradient(135deg,#00c8ff,#8b5cf6)', display: 'flex', alignItems: 'center', justifyContent: 'center', fontSize: 26, fontWeight: 800, color: '#000', flexShrink: 0 }}>
          {(form.firstName?.[0] ?? '?').toUpperCase()}{(form.lastName?.[0] ?? '').toUpperCase()}
        </div>
        <div style={{ flex: 1, minWidth: 0 }}>
          <div style={{ fontSize: '1.2rem', fontWeight: 800, color: 'var(--color-text)' }}>
            {form.firstName} {form.lastName}
          </div>
          <div style={{ fontSize: '0.85rem', color: 'var(--color-muted)', marginTop: 2 }}>{form.email}</div>
          {form.city && <div style={{ fontSize: '0.8rem', color: 'var(--color-muted2)', marginTop: 2 }}>📍 {form.city}{form.country ? `, ${form.country}` : ''}</div>}
        </div>
      </div>

      <form onSubmit={handleSave}>
        {/* Informations personnelles */}
        <div style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, padding: '1.75rem', marginBottom: '1.25rem' }}>
          <h2 style={{ fontSize: '0.8rem', fontWeight: 700, color: 'var(--color-muted)', letterSpacing: '0.06em', margin: '0 0 1.25rem' }}>INFORMATIONS PERSONNELLES</h2>
          <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fill, minmax(220px, 1fr))', gap: 14 }}>
            {inputField('PRÉNOM', 'firstName', 'text', 'Jean')}
            {inputField('NOM', 'lastName', 'text', 'Dupont')}
            {inputField('TÉLÉPHONE', 'phone', 'tel', '+32 xxx xx xx xx')}
            {inputField('ADRESSE', 'address', 'text', 'Rue de la Loi 1')}
            {inputField('VILLE', 'city', 'text', 'Bruxelles')}
            {inputField('PAYS', 'country', 'text', 'Belgium')}
          </div>
        </div>

        {/* Liens pro */}
        <div style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, padding: '1.75rem', marginBottom: '1.25rem' }}>
          <h2 style={{ fontSize: '0.8rem', fontWeight: 700, color: 'var(--color-muted)', letterSpacing: '0.06em', margin: '0 0 1.25rem' }}>LIENS PROFESSIONNELS</h2>
          <div style={{ display: 'flex', flexDirection: 'column', gap: 14 }}>
            {inputField('LINKEDIN URL', 'linkedInUrl', 'url', 'https://linkedin.com/in/votre-profil')}
            {inputField('GITHUB URL', 'gitHubUrl', 'url', 'https://github.com/votre-pseudo')}
            {inputField('CV (chemin ou URL)', 'cvPath', 'text', '/cv/mon-cv.pdf')}
          </div>
        </div>

        {/* Lettre de motivation */}
        <div style={{ background: 'var(--color-card)', border: '1px solid var(--color-border)', borderRadius: 14, padding: '1.75rem', marginBottom: '1.75rem' }}>
          <h2 style={{ fontSize: '0.8rem', fontWeight: 700, color: 'var(--color-muted)', letterSpacing: '0.06em', margin: '0 0 1.25rem' }}>LETTRE DE MOTIVATION PAR DÉFAUT</h2>
          <p style={{ fontSize: '0.8rem', color: 'var(--color-muted)', marginBottom: 12 }}>
            Pré-remplie automatiquement lors de vos candidatures. Vous pouvez la personnaliser à chaque postulation.
          </p>
          <textarea className="input-dark" rows={6}
            placeholder="Madame, Monsieur, je me permets de vous adresser ma candidature pour le poste de…"
            value={form.coverLetter ?? ''}
            onChange={set('coverLetter')}
            style={{ resize: 'vertical', fontFamily: 'var(--font-sans)', lineHeight: 1.6 }} />
        </div>

        <div style={{ display: 'flex', justifyContent: 'flex-end', gap: 12 }}>
          <button type="button" className="btn-ghost" onClick={() => router.push('/dashboard')}>Annuler</button>
          <button type="submit" className="btn-primary" disabled={saving} style={{ padding: '0.75rem 2.5rem', fontSize: '0.95rem' }}>
            {saving ? '⟳ Sauvegarde…' : '✓ Sauvegarder le profil'}
          </button>
        </div>
      </form>
    </div>
  );
}





















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































