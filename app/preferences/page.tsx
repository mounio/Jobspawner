'use client';
import { useState, useEffect } from 'react';
import { useRouter } from 'next/navigation';
import { usersApi } from '@/lib/api';
import { UserPreferenceDto, UserPreferenceCreateDto } from '@/lib/types';
import { useAuth } from '@/context/AuthContext';
import { useToast } from '@/context/ToastContext';

const JOB_TYPES   = ['Full-time', 'Part-time', 'Contract', 'Freelance'];
const TECH_PRESETS = ['React','Vue','Angular','Next.js','TypeScript','Node.js','Python','Django','Go','Rust','Java','Spring','C#','.NET','PHP','Laravel','Flutter','Swift','Kotlin','DevOps','Docker','Kubernetes','AWS','PostgreSQL','MongoDB'];

export default function PreferencesPage() {
  const { user, isLoading } = useAuth();
  const router = useRouter();
  const toast  = useToast();

  const [prefs, setPrefs] = useState<UserPreferenceDto>({ techStack:'', location:'', jobType:'Full-time', remoteAllowed:false });
  const [techs, setTechs] = useState<string[]>([]);
  const [techInput, setTechInput] = useState('');
  const [saving, setSaving]   = useState(false);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    if (isLoading) return;
    if (!user) { router.replace('/login'); return; }
    usersApi.preferences().then((p) => {
      if (p) {
        setPrefs(p);
        setTechs(p.techStack ? p.techStack.split(',').map(t => t.trim()).filter(Boolean) : []);
      }
    }).catch(()=>{}).finally(()=>setLoading(false));
  }, [user, isLoading]);

  const addTech = (tech: string) => {
    const t = tech.trim();
    if (!t || techs.includes(t)) return;
    setTechs(p=>[...p,t]);
    setTechInput('');
  };
  const removeTech = (t: string) => setTechs(p=>p.filter(x=>x!==t));

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setSaving(true);
    try {
      const payload: UserPreferenceCreateDto = { techStack: techs.join(', '), location: prefs.location ?? '', jobType: prefs.jobType ?? '', remoteAllowed: prefs.remoteAllowed ?? false };
      await usersApi.savePreferences(payload);
      toast('Préférences sauvegardées ✓');
      router.push('/dashboard');
    } catch(err:any){ toast(err.message ?? 'Erreur sauvegarde', 'error'); }
    finally { setSaving(false); }
  };

  if(loading) return (
    <div style={{maxWidth:680,margin:'0 auto',padding:'3rem 1.5rem'}}>
      {Array(5).fill(0).map((_,i)=><div key={i} className="skeleton" style={{height:60,marginBottom:16}}/>)}
    </div>
  );

  return (
    <div style={{maxWidth:680,margin:'0 auto',padding:'2.5rem 1.5rem'}}>
      <div style={{marginBottom:'2rem'}}>
        <h1 style={{fontSize:'1.75rem',fontWeight:900,margin:'0 0 6px',color:'var(--color-text)'}}>Mes préférences</h1>
        <p style={{color:'var(--color-muted)',fontSize:'0.88rem',margin:0}}>Personnalisez votre flux d&apos;offres pour les recommandations</p>
      </div>

      <form onSubmit={handleSubmit} style={{display:'flex',flexDirection:'column',gap:20}}>

        {/* Tech Stack */}
        <div style={{background:'var(--color-card)',border:'1px solid var(--color-border)',borderRadius:14,padding:'1.75rem'}}>
          <h2 style={{fontSize:'0.8rem',fontWeight:700,color:'var(--color-muted)',letterSpacing:'0.06em',margin:'0 0 1rem'}}>STACK TECHNIQUE</h2>

          {/* Selected tags */}
          <div style={{display:'flex',flexWrap:'wrap',gap:8,marginBottom:12,minHeight:36}}>
            {techs.map(t=>(
              <span key={t} style={{display:'inline-flex',alignItems:'center',gap:6,padding:'0.3rem 0.7rem',borderRadius:8,background:'rgba(139,92,246,0.15)',color:'#a78bfa',border:'1px solid rgba(139,92,246,0.3)',fontSize:'0.8rem',fontWeight:600,fontFamily:'var(--font-mono)'}}>
                {t}
                <button type="button" onClick={()=>removeTech(t)} style={{background:'none',border:'none',color:'#a78bfa',cursor:'pointer',padding:0,fontSize:'1rem',lineHeight:1}}>×</button>
              </span>
            ))}
          </div>

          {/* Input */}
          <div style={{display:'flex',gap:8,marginBottom:14}}>
            <input className="input-dark" value={techInput} onChange={e=>setTechInput(e.target.value)}
              placeholder="Ajouter une technologie…"
              onKeyDown={e=>{if(e.key==='Enter'){e.preventDefault();addTech(techInput);}}}
              style={{flex:1}}/>
            <button type="button" className="btn-ghost" onClick={()=>addTech(techInput)} style={{flexShrink:0}}>+</button>
          </div>

          {/* Presets */}
          <div>
            <p style={{fontSize:'0.72rem',color:'var(--color-muted)',marginBottom:8,fontWeight:600}}>SUGGESTIONS RAPIDES</p>
            <div style={{display:'flex',flexWrap:'wrap',gap:6}}>
              {TECH_PRESETS.filter(t=>!techs.includes(t)).map(t=>(
                <button key={t} type="button" onClick={()=>addTech(t)}
                  style={{padding:'0.25rem 0.6rem',borderRadius:6,fontSize:'0.75rem',fontWeight:500,background:'var(--color-surface)',border:'1px solid var(--color-border)',color:'var(--color-muted2)',cursor:'pointer',transition:'all 0.15s',fontFamily:'var(--font-mono)'}}
                  onMouseEnter={e=>{(e.currentTarget as HTMLElement).style.borderColor='var(--color-cyan-dim)';(e.currentTarget as HTMLElement).style.color='var(--color-cyan)'}}
                  onMouseLeave={e=>{(e.currentTarget as HTMLElement).style.borderColor='var(--color-border)';(e.currentTarget as HTMLElement).style.color='var(--color-muted2)'}}>
                  + {t}
                </button>
              ))}
            </div>
          </div>
        </div>

        {/* Location + JobType + Remote */}
        <div style={{background:'var(--color-card)',border:'1px solid var(--color-border)',borderRadius:14,padding:'1.75rem'}}>
          <h2 style={{fontSize:'0.8rem',fontWeight:700,color:'var(--color-muted)',letterSpacing:'0.06em',margin:'0 0 1.25rem'}}>PRÉFÉRENCES DE TRAVAIL</h2>
          <div style={{display:'grid',gridTemplateColumns:'1fr 1fr',gap:16,marginBottom:16}}>
            <div>
              <label style={{display:'block',fontSize:'0.72rem',fontWeight:700,color:'var(--color-muted)',marginBottom:6,letterSpacing:'0.05em'}}>LOCALISATION</label>
              <input className="input-dark" value={prefs.location} onChange={e=>setPrefs(p=>({...p,location:e.target.value}))} placeholder="Bruxelles, Remote, Paris…"/>
            </div>
            <div>
              <label style={{display:'block',fontSize:'0.72rem',fontWeight:700,color:'var(--color-muted)',marginBottom:6,letterSpacing:'0.05em'}}>TYPE DE CONTRAT</label>
              <select className="input-dark" value={prefs.jobType} onChange={e=>setPrefs(p=>({...p,jobType:e.target.value}))}>
                {JOB_TYPES.map(t=><option key={t} value={t}>{t}</option>)}
              </select>
            </div>
          </div>

          <label style={{display:'flex',alignItems:'center',gap:12,cursor:'pointer',padding:'1rem',borderRadius:10,border:`1px solid ${prefs.remoteAllowed?'rgba(16,185,129,0.4)':'var(--color-border)'}`,background:prefs.remoteAllowed?'rgba(16,185,129,0.06)':'var(--color-surface)',transition:'all 0.2s'}}>
            <div onClick={()=>setPrefs(p=>({...p,remoteAllowed:!p.remoteAllowed}))} style={{width:44,height:24,borderRadius:12,background:prefs.remoteAllowed?'#10b981':'var(--color-border)',position:'relative',transition:'background 0.2s',flexShrink:0,cursor:'pointer'}}>
              <div style={{position:'absolute',top:3,left:prefs.remoteAllowed?22:3,width:18,height:18,borderRadius:9,background:'white',transition:'left 0.2s'}}/>
            </div>
            <div>
              <div style={{fontSize:'0.9rem',fontWeight:700,color:prefs.remoteAllowed?'#10b981':'var(--color-text)'}}>Télétravail accepté</div>
              <div style={{fontSize:'0.78rem',color:'var(--color-muted)'}}>Inclure les offres full remote dans les recommandations</div>
            </div>
          </label>
        </div>

        {/* Actions */}
        <div style={{display:'flex',gap:12,justifyContent:'flex-end'}}>
          <button type="button" className="btn-ghost" onClick={()=>router.push('/dashboard')}>Annuler</button>
          <button type="submit" className="btn-primary" disabled={saving} style={{padding:'0.75rem 2.5rem',fontSize:'0.95rem'}}>
            {saving ? '⟳ Sauvegarde…' : '✓ Sauvegarder les préférences'}
          </button>
        </div>
      </form>
    </div>
  );
}





















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































